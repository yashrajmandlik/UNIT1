//Write a program for Relational operator


#include <stdio.h>

int main()   {

    int a,b;
    a=20;
    b=30;

        printf("%d\n", a < b);      //performing this (a<b)
        printf("%d\n", a <= b);     //performing this (a<=b)
        printf("%d\n", a > b);      //performing this (a>b)
        printf("%d\n", a >= b);     //performing this (a>=b)
        printf("%d\n", a == b);     //performing this (a==b)
        printf("%d\n", a != b);     //performing this (a!=b)


return 0;

}